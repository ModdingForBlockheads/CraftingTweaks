package net.blay09.mods.craftingtweaks.api;

public class ButtonPosition {
    private final int x;
    private final int y;

    public ButtonPosition(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
